class Produk {
  final String id;
  final String name;
  final String category;
  final String location;
  final int price;
  final String condition;
  final String publish_date;
  final String image;

  Produk({
    required this.id,
    required this.name,
    required this.category,
    required this.location,
    required this.price,
    required this.condition,
    required this.publish_date,
    required this.image,
  });
}
